package loopcontrol.demo1;

public class WhileDemo {

    public static void main(String[] args) {
        int counter = 0;
        while (counter != 10) { // Repeats a statement while a given condition is true. It tests the condition before executing the loop body.
            System.out.println(counter++);
        }

        // https://www.tutorialspoint.com/java/java_while_loop.htm
    }
}
